'use client'

import { useState, useEffect, useCallback, useRef } from 'react'
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card'
import { Separator } from '@/components/ui/separator'
import { CommentForm } from '@/components/comment/comment-form'
import { CommentList } from '@/components/comment/comment-list'
import { Comment, CommentFormData } from '@/types/comment'
import { cn, debounce } from '@/lib/utils'
import { getComments, createComment, createReply } from '@/lib/actions/comment'
import { toggleCommentLike } from '@/lib/actions/comment/comment-like'
import { useToast } from '@/hooks/use-toast'

interface ProjectCommentsProps {
  projectId: string
  initialComments?: Comment[]
  className?: string
}

export function ProjectComments({ 
  projectId, 
  initialComments = [], 
  className 
}: ProjectCommentsProps) {
  const [comments, setComments] = useState<Comment[]>(initialComments)
  const [isLoading, setIsLoading] = useState(false)
  const [isSubmitting, setIsSubmitting] = useState(false)
  const [likingComments, setLikingComments] = useState<Set<string>>(new Set())
  const { toast } = useToast()

  const loadComments = useCallback(async () => {
    setIsLoading(true)
    try {
      const result = await getComments({
        projectId,
        page: 1,
        limit: 20,
        sortBy: 'latest'
      })
      
      if (result.success && result.data) {
        setComments(result.data.comments || [])
      }
    } catch (error) {
      console.error('ÎåìÍ∏Ä Î°úÎìú Ïã§Ìå®:', error)
      setComments(initialComments)
    } finally {
      setIsLoading(false)
    }
  }, [projectId, initialComments])

  // Ïª¥Ìè¨ÎÑåÌä∏ ÎßàÏö¥Ìä∏Ïãú ÏµúÏã† ÎåìÍ∏Ä Î°úÎìú
  useEffect(() => {
    loadComments()
  }, [projectId, initialComments, loadComments])

  const handleCommentSubmit = async (data: CommentFormData) => {
    setIsSubmitting(true)
    try {
      // Handle top-level comments
      if (!data.parentId) {
        const result = await createComment({
          projectId,
          content: data.content
        })
        
        if (result.success && result.data) {
          // ÏÉà ÎåìÍ∏ÄÏùÑ Î™©Î°ù ÏÉÅÎã®Ïóê Ï∂îÍ∞Ä
          setComments(prev => [result.data!, ...prev])
        } else {
          throw new Error(result.error || 'ÎåìÍ∏Ä ÏûëÏÑ±Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§')
        }
      } else {
        // Handle replies (this shouldn't happen from main form, but just in case)
        const result = await createReply({
          content: data.content,
          parentId: data.parentId
        })
        
        if (result.success && result.data) {
          // Refresh comments to show the new reply
          await loadComments()
        } else {
          throw new Error(result.error || 'ÎãµÍ∏Ä ÏûëÏÑ±Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§')
        }
      }
    } catch (error) {
      console.error('ÎåìÍ∏Ä ÏûëÏÑ± Ïã§Ìå®:', error)
      throw error
    } finally {
      setIsSubmitting(false)
    }
  }

  const handleReplyCreated = async (reply: Comment) => {
    // Optimistically update the comments structure
    setComments(prev => {
      const updateCommentReplies = (comments: Comment[]): Comment[] => {
        return comments.map(comment => {
          if (comment.id === reply.parentId) {
            // Found the parent comment, add the reply
            return {
              ...comment,
              replies: [...(comment.replies || []), reply],
              repliesCount: comment.repliesCount + 1
            }
          } else if (comment.replies && comment.replies.length > 0) {
            // Recursively check nested replies
            return {
              ...comment,
              replies: updateCommentReplies(comment.replies)
            }
          }
          return comment
        })
      }
      
      return updateCommentReplies(prev)
    })
  }

  // Helper function to find and update comment by ID
  const findAndUpdateComment = useCallback((
    comments: Comment[], 
    commentId: string, 
    updateFn: (comment: Comment) => Comment
  ): Comment[] => {
    return comments.map(comment => {
      if (comment.id === commentId) {
        return updateFn(comment)
      }
      if (comment.replies?.length) {
        return {
          ...comment,
          replies: findAndUpdateComment(comment.replies, commentId, updateFn)
        }
      }
      return comment
    })
  }, [])

  // Enhanced comment like handler with optimistic updates and error recovery
  const handleCommentLike = useCallback(async (commentId: string) => {
    console.log('üî• handleCommentLike called with commentId:', commentId)
    console.log('üî• Current likingComments set:', Array.from(likingComments))
    
    // Prevent multiple likes on same comment
    if (likingComments.has(commentId)) {
      console.log('‚ö†Ô∏è Already liking this comment, returning early')
      return
    }

    // Find the comment to get current state
    const findComment = (comments: Comment[], id: string): Comment | null => {
      console.log('üîç findComment searching for:', id, 'in', comments.length, 'comments')
      for (const comment of comments) {
        console.log('  - Checking comment:', comment.id, 'hasReplies:', !!comment.replies?.length)
        if (comment.id === id) {
          console.log('  ‚úÖ Found comment directly:', comment.id)
          return comment
        }
        if (comment.replies?.length) {
          console.log('  üîç Searching in', comment.replies.length, 'replies...')
          const found = findComment(comment.replies, id)
          if (found) {
            console.log('  ‚úÖ Found comment in replies:', found.id)
            return found
          }
        }
      }
      console.log('  ‚ùå Comment not found in this level')
      return null
    }

    const currentComment = findComment(comments, commentId)
    console.log('üî• Found current comment:', currentComment)
    console.log('üî• Current comments state:', comments)
    console.log('üî• Comments count:', comments.length)
    
    if (!currentComment) {
      console.log('‚ùå Comment not found, returning early')
      return
    }

    // Add to liking set
    setLikingComments(prev => {
      const newSet = new Set([...prev, commentId])
      console.log('üî• Adding to liking set. New set:', Array.from(newSet))
      return newSet
    })

    // Store original state for potential rollback
    const originalIsLiked = currentComment.isLiked
    const originalLikeCount = currentComment.likeCount
    console.log('üî• Original state - isLiked:', originalIsLiked, 'likeCount:', originalLikeCount)

    // Optimistic update
    const newIsLiked = !originalIsLiked
    const newLikeCount = originalIsLiked ? originalLikeCount - 1 : originalLikeCount + 1
    console.log('üî• Optimistic update - newIsLiked:', newIsLiked, 'newLikeCount:', newLikeCount)

    setComments(prev => {
      const updated = findAndUpdateComment(prev, commentId, (comment) => ({
        ...comment,
        isLiked: newIsLiked,
        likeCount: newLikeCount
      }))
      console.log('üî• Optimistic update applied to comments')
      return updated
    })

    try {
      console.log('üî• Calling toggleCommentLike action...')
      const result = await toggleCommentLike({ commentId })
      console.log('üî• toggleCommentLike result:', result)

      if (result.success && result.data) {
        console.log('‚úÖ Server response success:', result.data)
        // Update with server response
        setComments(prev => {
          const updated = findAndUpdateComment(prev, commentId, (comment) => ({
            ...comment,
            isLiked: result.data!.isLiked,
            likeCount: result.data!.likeCount
          }))
          console.log('üî• Server response applied to comments')
          return updated
        })
      } else {
        console.log('‚ùå Server response failed:', result.error)
        // Revert optimistic update
        setComments(prev => {
          const reverted = findAndUpdateComment(prev, commentId, (comment) => ({
            ...comment,
            isLiked: originalIsLiked,
            likeCount: originalLikeCount
          }))
          console.log('üî• Optimistic update reverted')
          return reverted
        })
        
        toast({
          title: "Ï¢ãÏïÑÏöî Ïã§Ìå®",
          description: result.error || "Ï¢ãÏïÑÏöî Ï≤òÎ¶¨ Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.",
          variant: "destructive"
        })
      }
    } catch (error) {
      console.log('‚ùå Exception caught in handleCommentLike:', error)
      // Revert optimistic update
      setComments(prev => {
        const reverted = findAndUpdateComment(prev, commentId, (comment) => ({
          ...comment,
          isLiked: originalIsLiked,
          likeCount: originalLikeCount
        }))
        console.log('üî• Optimistic update reverted due to exception')
        return reverted
      })
      
      toast({
        title: "ÎÑ§Ìä∏ÏõåÌÅ¨ Ïò§Î•ò",
        description: "Ïù∏ÌÑ∞ÎÑ∑ Ïó∞Í≤∞ÏùÑ ÌôïÏù∏ÌïòÍ≥† Îã§Ïãú ÏãúÎèÑÌï¥Ï£ºÏÑ∏Ïöî.",
        variant: "destructive"
      })
    } finally {
      // Remove from liking set
      setLikingComments(prev => {
        const newSet = new Set(prev)
        newSet.delete(commentId)
        console.log('üî• Removed from liking set. New set:', Array.from(newSet))
        return newSet
      })
    }
  }, [comments, likingComments, findAndUpdateComment, toast])

  // Debounced like handler to prevent rapid clicking
  const debouncedLikeHandler = useRef(
    debounce(handleCommentLike, 300)
  ).current

  const handleLoadMore = () => {
    // Mock load more functionality
    console.log('Load more comments...')
  }

  return (
    <Card className={cn("w-full", className)}>
      <CardHeader>
        <CardTitle className="flex items-center gap-2">
          ÎåìÍ∏Ä ({comments.length})
        </CardTitle>
      </CardHeader>
      <CardContent className="space-y-6">
        {/* ÎåìÍ∏Ä ÏûëÏÑ± Ìèº */}
        <CommentForm 
          onSubmit={handleCommentSubmit}
          isLoading={isSubmitting}
        />
        
        <Separator />
        
        {/* ÎåìÍ∏Ä Î™©Î°ù */}
        <CommentList 
          comments={comments}
          isLoading={isLoading}
          onLoadMore={handleLoadMore}
          hasMore={false}
          onReplyCreated={handleReplyCreated}
          onLike={debouncedLikeHandler}
        />
      </CardContent>
    </Card>
  )
}